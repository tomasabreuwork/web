    function updateSwitcherUI(lang) {
        const currentImg = document.querySelector('.lang-current img');
        if (currentImg) {
            currentImg.src = FLAGS[lang].src;
            currentImg.alt = FLAGS[lang].alt;
        }
    }

    function buildSwitcher() {
        const currentLang = getCurrentLang();
        const otherLangs = Object.keys(FLAGS).filter(l => l !== currentLang);

        const switcher = document.createElement('div');
        switcher.className = 'lang-switcher';
        switcher.innerHTML = `
            <div class="lang-options">
                ${otherLangs.map(l => `
                    <button class="lang-option" data-lang="${l}" title="${FLAGS[l].alt}" aria-label="${FLAGS[l].alt}">
                        <img src="${FLAGS[l].src}" alt="${FLAGS[l].alt}">
                    </button>
                `).join('')}
            </div>
            <button class="lang-current" aria-label="Mudar idioma">
                <img src="${FLAGS[currentLang].src}" alt="${FLAGS[currentLang].alt}">
            </button>
        `;

        document.body.appendChild(switcher);

        // Toggle open/close
        switcher.querySelector('.lang-current').addEventListener('click', (e) => {
            e.stopPropagation();
            switcher.classList.toggle('open');
        });

        // Close on outside click
        document.addEventListener('click', () => {
            switcher.classList.remove('open');
        });

        switcher.addEventListener('click', e => e.stopPropagation());

        // Language option buttons
        switcher.querySelectorAll('.lang-option').forEach(btn => {
            btn.addEventListener('click', () => {
                const lang = btn.dataset.lang;
                switcher.classList.remove('open');

                // Rebuild options (exclude newly selected lang)
                setLang(lang);

                // Update options list
                const newOthers = Object.keys(FLAGS).filter(l => l !== lang);
                const optionsContainer = switcher.querySelector('.lang-options');
                optionsContainer.innerHTML = newOthers.map(l => `
                    <button class="lang-option" data-lang="${l}" title="${FLAGS[l].alt}" aria-label="${FLAGS[l].alt}">
                        <img src="${FLAGS[l].src}" alt="${FLAGS[l].alt}">
                    </button>
                `).join('');

                // Re-attach events for new buttons
                optionsContainer.querySelectorAll('.lang-option').forEach(newBtn => {
                    newBtn.addEventListener('click', () => {
                        const newLang = newBtn.dataset.lang;
                        switcher.classList.remove('open');
                        setLang(newLang);
                        rebuildOptions(switcher, newLang);
                    });
                });
            });
        });
    }

    function rebuildOptions(switcher, currentLang) {
        const newOthers = Object.keys(FLAGS).filter(l => l !== currentLang);
        const optionsContainer = switcher.querySelector('.lang-options');
        optionsContainer.innerHTML = newOthers.map(l => `
            <button class="lang-option" data-lang="${l}" title="${FLAGS[l].alt}" aria-label="${FLAGS[l].alt}">
                <img src="${FLAGS[l].src}" alt="${FLAGS[l].alt}">
            </button>
        `).join('');

        optionsContainer.querySelectorAll('.lang-option').forEach(btn => {
            btn.addEventListener('click', () => {
                const lang = btn.dataset.lang;
                switcher.classList.remove('open');
                setLang(lang);
                rebuildOptions(switcher, lang);
            });
        });
    }

    // Init
    document.addEventListener('DOMContentLoaded', () => {
        buildSwitcher();
        const saved = getCurrentLang();
        if (saved !== 'pt') {
            applyTranslations(saved);
        }
    });

